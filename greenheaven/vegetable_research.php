            	<h4>History of vegetables</h4>

<p>Before the advent of agriculture, humans were hunter-gatherers. They foraged for edible fruit, nuts, stems, leaves, corms, and tubers, scavenged for dead animals and hunted living ones for food. Forest gardening in a tropical jungle clearing is thought to be the first example of agriculture; useful plant species were identified and encouraged to grow while undesirable species were removed.</p><p>Plant breeding through the selection of strains with desirable traits such as large fruit and vigorous growth soon followed. While the first evidence for the domestication of grasses such as wheat and barley has been found in the Fertile Crescent in the Middle East, it is likely that various peoples around the world started growing crops in the period 10,000 BC to 7,000 BC. Subsistence agriculture continues to this day, with many rural farmers in Africa, Asia, South America, and elsewhere using their plots of land to produce enough food for their families, while any surplus produce is used for exchange for other goods.</p>

<p>Throughout recorded history, the rich have been able to afford a varied diet including meat, vegetables and fruit, but for poor people, meat was a luxury and the food they ate was very dull, typically comprising mainly some staple product made from rice, rye, barley, wheat, millet or maize. The addition of vegetable matter provided some variety to the diet. The staple diet of the Aztecs in Central America was maize and they cultivated tomatoes, avocados, beans, peppers, pumpkins, squashes, peanuts, and amaranth seeds to supplement their tortillas and porridge. In Peru, the Incas subsisted on maize in the lowlands and potatoes at higher altitudes. They also used seeds from quinoa, supplementing their diet with peppers, tomatoes, and avocados.</p>

<p>In Ancient China, rice was the staple crop in the south and wheat in the north, the latter made into dumplings, noodles, and pancakes. Vegetables used to accompany these included yams, soybeans, broad beans, turnips, spring onions, and garlic. The diet of the ancient Egyptians was based on bread, often contaminated with sand which wore away their teeth. Meat was a luxury but fish was more plentiful. These were accompanied by a range of vegetables including marrows, broad beans, lentils, onions, leeks, garlic, radishes, and lettuces. </p>          
