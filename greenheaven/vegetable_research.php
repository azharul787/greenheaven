            	<h4>Dietary recommendations</h4>

<p>The USDA Dietary Guidelines for Americans recommends consuming five to nine servings of fruit and vegetables daily. The total amount consumed will vary according to age and gender, and is determined based upon the standard portion sizes typically consumed, as well as general nutritional content.</p><p> Potatoes are not included in the count as they are mainly providers of starch. For most vegetables and vegetable juices, one serving is half of a cup and can be eaten raw or cooked. For leafy greens, such as lettuce and spinach, a single serving is typically a full cup. A variety of products should be chosen as no single fruit or vegetable provides all the nutrients needed for health.</p>

<p>International dietary guidelines are similar to the ones established by the USDA. Japan, for example, recommends the consumption of five to six servings of vegetables daily. French recommendations provide similar guidelines and set the daily goal at five servings. In India, the daily recommendation for adults is 275 grams (9.7 oz) of vegetables per day.  </p>          </div>
