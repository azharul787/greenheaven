            	<h4>Nutrition and health</h4><br>

<p>Vegetables play an important role in human nutrition. Most are low in fat and calories but are bulky and filling.[18] They supply dietary fiber and are important sources of essential vitamins, minerals, and trace elements. Particularly important are the antioxidant vitamins A, C, and E. When vegetables are included in the diet, there is found to be a reduction in the incidence of cancer, stroke, cardiovascular disease, and other chronic ailments.</p><p> Research has shown that, compared with individuals who eat less than three servings of fruits and vegetables each day, those that eat more than five servings have an approximately twenty percent lower risk of developing coronary heart disease or stroke. The nutritional content of vegetables varies considerably; some contain useful amounts of protein though generally they contain little fat, and varying proportions of vitamins such as vitamin A, vitamin K, and vitamin B6; provitamins; dietary minerals; and carbohydrates.</p>

<p>However, vegetables often also contain toxins and antinutrients which interfere with the absorption of nutrients. These include α-solanine, α-chaconine, enzyme inhibitors (of cholinesterase, protease, amylase, etc.), cyanide and cyanide precursors, oxalic acid, and others.[citation needed] These toxins are natural defenses, used to ward off the insects, predators and fungi that might attack the plant. Some beans contain phytohaemagglutinin, and cassava roots contain cyanogenic glycoside as do bamboo shoots. These toxins can be deactivated by adequate cooking. Green potatoes contain glycoalkaloids and should be avoided.</p>
