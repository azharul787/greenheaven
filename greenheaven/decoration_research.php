                    <p>Ornamental or decoration are plants that are grown for decorative purposes in gardens and landscape design projects, as houseplants, for cut flowers and specimen display. The cultivation of these, called floriculture, forms a major branch of horticulture.</p><br><p>Commonly, ornamental [garden] plants are grown for the display of aesthetic features including: flowers, leaves, scent, overall foliage texture, fruit, stem and bark, and aesthetic form. In some cases, unusual features may be considered to be of interest, such as the prominent thorns of Rosa sericea and cacti. In all cases, their purpose is for the enjoyment of gardeners, visitors, and the public institutions.</p><br><p>Ornamental grasses are grasses grown as decoration plants. Many decoration grasses are true grasses (Poaceae), however several other families of grass-like plants are typically marketed as decoration grasses. These include the sedges (Cyperaceae), rushes (Juncaceae), restios (Restionaceae), and cat-tails (Typhaceae). All are monocotyledons, typically with narrow leaves and parallel veins. Most are herbaceous perennials, though many are evergreen and some develop woody tissues. decoration grasses are popular in many They bring striking linear form, texture, color, motion, and sound to the garden, throughout the year.</p>             
			
            	
