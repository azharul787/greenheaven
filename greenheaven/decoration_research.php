                <p>Similarly certain trees may be called decoration trees. This term is used when they are used as part of a garden, park, or landscape setting, for instance for their flowers, their texture, form ,size and shape, and other aesthetic characteristics. In some countries trees in 'utilitarian' landscape use such as screening, and roadside plantings are called amenity trees. </p>         
