            	<h4>The term</h4>

                <p>The term decoration plant is used here in the same sense that it is generally used in the horticultural trades.[2][3] The term largely corresponds to 'garden plant', though the latter is much less precise, as any plant may be grown in a garden. decoration plants are plants which are grown for display purposes, rather than functional ones. While some plants are both ornamental and functional, people usually use the term “decoration plants” to refer to plants which have no value beyond being attractive, although many people feel that this is value enough. decoration plants are the keystone of ornamental gardening, and they come in a range of shapes, sizes and colors suitable to a broad array of climates, landscapes, and gardening needs.</p>
                <p>Some decoration plants are grown for showy foliage. Their foliage may be deciduous, turning bright orange, red, and yellow before dropping off in the fall, or evergreen, in which case it stays green year-round. Some decoration foliage has a striking appearance created by lacy leaves or long needles, while other decoration are grown for distinctively colored leaves, such as silvery-gray ground covers and bright red grasses, among many others.
                Other ornamental plants are cultivated for their blooms. Flowering decoration are a key aspect of many gardens, with many flower gardeners preferring to plant a variety of flowers so that the garden is continuously in flower through the spring and summer. Depending on the types of plants being grown, the flowers may be subtle and delicate, or large and showy, with some decoration plants producing distinctive aromas which paint a palette of scents in addition to colors.</p>         
