                   <p>For plants to be considered to be ornamental, they may require specific work and activity by a gardener. For instance, many plants cultivated for topiary and bonsai would only be considered to be ornamental by virtue of the regular pruning carried out on them by the gardener, and they may rapidly cease to be decoration if the work was abandoned.</p>
                   <p>Decoration plants and trees are distinguished from utilitarian and crop plants, such as those used for agriculture and vegetable crops, and for forestry or as fruit trees. This does not preclude any particular type of plant being grown both for decoration qualities in the garden, and for utilitarian purposes in other settings. Thus lavender is typically grown as an ornamental plant in gardens, but may also be grown as a crop plant for the production of lavender oil.</p>
             
