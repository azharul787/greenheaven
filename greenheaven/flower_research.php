            	<h4>Terms Defining Flower Parts</h4><br>
				A flower having sepals, petals, stamens, and pistils is complete; lacking one or more of such structures, it is said to be incomplete. Stamens and pistils are not present together in all flowers.
				When both are present the flower is said to be perfect, or bisexual, regardless of a lack of any other part that renders it incomplete.<br><br>
				<b>Terms Referring to Plants</b>
				<ul class="flower_research_parts">
					<li> Hermaphroditic – Plants with perfect flowers (apples, tulips)</li>
					<li>Monoecious – Plants with separate male flowers and female flowers on the same plant (corn, squash, and pine)</li>
					<li>Dioecious – Plants with male flowers and female flowers on separate plants (maple, holly, and salt brush)</li>
					<li> Gynoecious – Plants with only female flowers</li>
					<li> Andromonoecious – Plants with only male flowers</li>
				</ul>
